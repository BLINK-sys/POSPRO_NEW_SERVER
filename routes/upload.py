            new_doc = ProductDocument(
                product_id=product_id,
                filename=filename,
                url=file_url,
                file_type='doc',
                mime_type=mime_type
            )
            
            try:
                db.session.add(new_doc)
                db.session.commit()
                print(f"–°–æ–∑–¥–∞–Ω–∞ –∑–∞–ø–∏—Å—å –≤ –ë–î –¥–ª—è –¥–æ–∫—É–º–µ–Ω—Ç–∞: {filename}")
            except Exception as e:
                print(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –∑–∞–ø–∏—Å–∏ –¥–ª—è –¥–æ–∫—É–º–µ–Ω—Ç–∞ {filename}: {e}")
